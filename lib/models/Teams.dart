
class StandingData {
   final int position;
   final String teamName;
   final int playedGames;
   final int won;
   final int drawn;
   final int lost;
   final int points;
   final int goalDifference;

   StandingData({
      required this.position,
      required this.teamName,
      required this.playedGames,
      required this.won,
      required this.drawn,
      required this.lost,
      required this.points,
      required this.goalDifference,
   });
}
